// Script to cleanup unnecessary node_modules and reduce repo size
const fs = require('fs');
const path = require('path');

// Recursively delete node_modules inside nested folders except root
function cleanNodeModules(dir) {
  const files = fs.readdirSync(dir);
  for (const file of files) {
    const fullPath = path.join(dir, file);
    if (file === 'node_modules' && dir !== process.cwd()) {
      console.log(`Removing: ${fullPath}`);
      fs.rmSync(fullPath, { recursive: true, force: true });
    } else if (fs.statSync(fullPath).isDirectory()) {
      cleanNodeModules(fullPath);
    }
  }
}

cleanNodeModules(process.cwd());
console.log('Cleanup complete.');
